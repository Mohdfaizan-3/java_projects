package com.uber.uberApp.entities.enums;

/**
 * Represents the different roles available in the UberApp system.
 */
public enum Role {
    /**
     * Administrator role with full system access.
     */
    ADMIN,

    /**
     * Driver role for users who provide transportation services.
     */
    DRIVER,

    /**
     * Rider role for users who request transportation services.
     */
    RIDER
}